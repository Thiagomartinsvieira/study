package Fundamentals;

public class Inference {
    public static void main(String[] args) {

        double a = 4.5;
        System.out.println(a);

        a = 12;
        System.out.println(a);

        var b = 4.5;
        System.out.println(b);

        var c = "Text";
        System.out.println(c);

        c = "Other text";
        System.out.println(c);

        double d;
        d = 123.65;
        System.out.println(d);

        var e = 123.4;
        System.out.println(e);

        var f = 12;
//        f = 12.4;
        System.out.println(f);
    }
}
